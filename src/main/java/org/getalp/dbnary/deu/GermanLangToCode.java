	public static String triletterCode(String s){ 
		if(s!=null && s!="") {
			s= s.trim();
			
			String resultat;
			if (ISO639_3.sharedInstance.getIdCode(s) != null) {
				resultat =ISO639_3.sharedInstance.getIdCode(s);
			}else{
				if(germanLangNameToLangCode.containsKey(s)){
					s=germanLangNameToLangCode.get(s);
					
						resultat =ISO639_3.sharedInstance.getIdCode(s);
					
				}else {
					resultat=null;
				}
			}
			return resultat;
		}else{

			return s;
		}
	}

