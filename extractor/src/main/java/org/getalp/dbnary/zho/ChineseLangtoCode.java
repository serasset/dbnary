	public static String triletterCode(String s){ 
		if(s!=null && s!="") {
			s= s.trim();
			s=s.toLowerCase();
			String resultat;
			if (ISO639_3.sharedInstance.getIdCode(s) != null) {
				resultat =ISO639_3.sharedInstance.getIdCode(s);
			}else{
				if(chineseLangNameToLangCode.containsKey(s)){
					s=chineseLangNameToLangCode.get(s);
					
						resultat =ISO639_3.sharedInstance.getIdCode(s);
					
				}else {
					resultat=null;
				}
			}
			return resultat;
		}else{

			return s;
		}
	}

	

